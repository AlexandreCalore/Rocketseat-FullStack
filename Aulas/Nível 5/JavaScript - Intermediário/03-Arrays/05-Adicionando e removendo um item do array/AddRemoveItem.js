let users = []
console.log(users)

users.push("Alexandre")
users.push("Ricardo")
users.push("Calore") // Estamos adicionando no final da lista
console.log(users)

users.unshift("Alexandre") // Adiciona o item no inícios do array
console.log(users)

users.shift() // Este método remove o item do início do array
console.log(users)

users.pop() // Ja este, remove do final do array
console.log(users)
